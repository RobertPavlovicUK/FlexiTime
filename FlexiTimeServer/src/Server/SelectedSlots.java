package Server;

public class SelectedSlots {
	int dayI;
	int hoursI;
	String BookedBy;
	
	
	public String getBookedBy() {
		return BookedBy;
	}
	public void setBookedBy(String BookedBy) {
		this.BookedBy = BookedBy;
	}
	public int getDayI() {
		return dayI;
	}
	public void setDayI(int dayI) {
		this.dayI = dayI;
	}
	public int getHoursI() {
		return hoursI;
	}
	public void setHoursI(int hoursI) {
		this.hoursI = hoursI;
	}

}
